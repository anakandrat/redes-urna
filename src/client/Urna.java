    private void jButtonVotarActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButtonVotarActionPerformed

        if (!(jTextFieldVoto.getText().equalsIgnoreCase("voto invalido"))) {
            //guardar o voto na variavel stringVoto.
            if (jTextFieldVoto.getText().equalsIgnoreCase("Voto em Branco")) {
                stringVoto = "1000";

            } else if (jTextFieldVoto.getText().equalsIgnoreCase("Voto Nulo")) {
                stringVoto = "0";
            } else {
                stringVoto = jTextFieldVoto.getText();

            }

            jTextFieldVoto.setText("");


            ctr.processVote(stringVoto);


        } else {

            stringVoto = "";
        }

    }//GEN-LAST:event_jButtonVotarActionPerformed

