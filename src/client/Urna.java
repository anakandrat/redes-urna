    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //compara o texto digitado com a string voto , e caso seja igual, preenchera com o 2
        //caso contrário, irá concatenar com o valor da string atual em jtextfieldvoto + 2
        if (jTextFieldVoto.getText().equalsIgnoreCase("voto invalido") || jTextFieldVoto.getText().equalsIgnoreCase("Voto Em Branco") || jTextFieldVoto.getText().equalsIgnoreCase("Voto Nulo")) {
            jTextFieldVoto.setText("2");

        } else {
            jTextFieldVoto.setText(jTextFieldVoto.getText() + "2");
        }
        //converter em inteiro para poder comparar o valor digitado e comparar com
        //um valor limite para poder limitar o numero de votos digitados na tela.
        //no caso o valor máximo aceito é 999 e o minimo é 1
        int comp = Integer.parseInt(jTextFieldVoto.getText());
        if (comp > 999 || comp < 1) {
            jTextFieldVoto.setText("voto invalido");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

